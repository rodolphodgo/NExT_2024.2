"""Faça um programa que converta as seguites temperaturas graus Celsius para graus Fahrenheit:
23°C
44°C
90°C
Fórmula para converter graus Fahrenheit para Celsius:
°F=(°C×9/5)+32"""

print('23°C em °F =', ((23 * 9 / 5) + 32), '°F')
print('44°C em °F =', ((44 * 9 / 5) + 32), '°F')
print('90°C em °F =', ((90 * 9 / 5) + 32), '°F')