"""Faça um programa que converta as seguintes temperatura em graus Fahrenheit para graus Celsius:
-40°F
-10°F
23°F
Fórmula para converter graus Fahrenheit para Celsius:
°C=5×((°F−32)/9)
"""

print('-40°F em °C =', (5 * (-40 - 32) / 9), 'C°')
print('-10°F em °C =', (5 * (-10 - 32) / 9), 'C°')
print('23°F em °C =', (5 * (23 - 32) / 9), 'C°')